// Function to get suggestions based on partial input
export function getSuggestions(partial: string, limit: number = 5): string[] {
  if (!partial || partial.length < 1) return [];
  
  console.log('Getting suggestions for:', partial);
  
  // For Bangla text, we don't need to lowercase
  const matches = banglaDictionary.filter(word => 
    word.startsWith(partial) && word !== partial
  );
  
  console.log('Matched words:', matches);
  
  // Sort by length (shorter words first) and alphabetically
  matches.sort((a, b) => {
    if (a.length !== b.length) {
      return a.length - b.length;
    }
    return a.localeCompare(b, 'bn');
  });
  
  return matches.slice(0, limit);
}

// Function to check if a word exists in dictionary
export function isWordInDictionary(word: string): boolean {
  return banglaDictionary.some(w => w.toLowerCase() === word.toLowerCase());
}